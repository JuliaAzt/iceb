                                        <p>   &nbsp;&nbsp;The Institute of Exact and Biological Sciences (ICEB) was created on July 16, 1981, aiming to contribute to the basic cycle of Engineering, Pharmacy and Nutrition courses at the Federal University of Ouro Preto (UFOP). However, in the last 35 years, the Institute started to attend directly (eg classes and practical activities in the ICEB premises) or indirectly the new courses that were created in this Institution of Education, mainly, after the Program of Restructuring and Expansion of the Federal Universities (REUNI) of the Federal Government. In addition to contributing in the context of teaching, the quality of its undergraduate courses, the research developed at ICEB has contributed to an exponential scientific growth, both nationally and internationally. Another contribution of the Institute has been the training of young people entering the undergraduate and baccalaureate courses from different cities, especially the cities and districts of Ouro Preto, Mariana, Itabirito, Ouro Branco and Lafaiete. This qualification, together with other activities developed by the Institute, mainly of an extensionist nature, has a direct reflection on the social equity of the region.</p>
